public class PrimitiveTypes {
    public static void main(String[] args) {
        // Integers
        byte numberOne = 127;
        short numberTwo = 32_000;
        int numberThree = 2_000_000_000;
        long numberFour = 9_999_999_99_999_999_999L;
        // Decimals
        float decimalOne = 10.99F;
        double decimalTwo = 9999.999999999;
        // Char & Boolean
        char letter = 'A';
        boolean trueFalse = true;
    }
}
